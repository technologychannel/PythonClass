# Variable: Container to store value.

# Syntax:
# variable_name = value

# Here name is variable and Bishwroaj Poudel is value
name = "Bishworaj Poudel" 
# print(name)

num1 = 10
num2 = 3


sum = num1 + num2  # 13 
diff = num1 - num2 # 7
mul = num1 * num2 # 30
div = num1 / num2 # 3.333333

# Mod is remainder after division
mod = num1 % num2 
intdiv = num1 // num2
cube = num1 ** 3

print(f"Sum is {sum} and diff is {diff}") # 13 7
print(f"Mul is {mul}") # 30
print(f"Div is {div}") # 3.333
print(f"Mod is {mod}") # 1
print(f"Int div is {intdiv}") # 3
print(f"Cube is {cube}") # 1000

